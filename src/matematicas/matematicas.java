package matematicas;

public class matematicas {
    public static void main(String[] args) {
        // Valor Obsoluto
        System.out.println(Math.abs(-20));

        // Redondeea el valor
        System.out.println(Math.round(10.5));

        // Redonde hacia arriba sin importar el valor
        System.out.println(Math.ceil(10.2));

        // Redonde hacia abajo sin importar el valor
        System.out.println(Math.floor(10.8));

        // Rncuentra el valor minimo entre 2 valores
        System.out.println(Math.min(10, 4));

        // Rncuentra el valor maximo entre 2 valores
        System.out.println(Math.min(10, 4));

        // Valor PI
        System.out.println(Math.PI);

        // Eleva una base a un Exponente
        System.out.println(Math.pow(2.2, 3.5));

        // Raiz cuadrada de un numero
        System.out.println(Math.sqrt(9));
    }
}
